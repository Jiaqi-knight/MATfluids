%% Write2File
function [] = Write2File(fname, varnames, varargin)

fid = fopen([fname '.dat'], 'w');

if ~isempty(varnames)
    fprintf(fid, '%s ', varnames{:});
    fprintf(fid, '\n');
end
data = cell2mat(varargin);
fprintf(fid, [repmat('%12.8f ', 1, size(data,2)) '\n'], data.');
fprintf(fid, '\n');
fclose(fid);

